package rensyu;

public class tokuren17 {

	public static void main(String[] args) {
		double flr = Math.floor(1.6);// 切り下げ
		double rnd = Math.round(1.5);// 四捨五入
		double dow = Math.ceil(1.1);// 切り上げ
		double pow = Math.pow(2, 2);// 累乗
		double sqrt = Math.sqrt(25);// 平方根
		double rad = 10 * 10 * Math.PI;// 円周率

		System.out.println(flr);
		System.out.println(rnd);
		System.out.println(dow);
		System.out.println(pow);
		System.out.println(sqrt);
		System.out.println(rad);
		
		//final static → ≪定数≫

	}

}
